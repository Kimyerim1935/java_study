
public class Method {
	//plus라는 매소드는 두개의정수를 입력받아(a.b) 결과로 정수를 내어놓는(리턴하는)메서드다.
	int plus (int a, int b )
		{ 
		return a+b; //a,b는 지역변수  데이터 타입을 맞춰주어야 한다.
		}
	int minus (int C, int D )
		{ 
			return C-D;	
		}
	int gobhaki (int a, int b )
		{ 
		return a*b;
		}
	int nanugi (int a, int b)
		{
		return a/b;
		}
}
